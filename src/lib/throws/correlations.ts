// ── Lookup functions ────────────────────────────────────────────────

/** Get all correlations for a specific event, gender, and distance band */
export function getCorrelations(
  event: EventCode,
  gender: GenderCode,
  band: string
): CorrelationEntry[] {
  const [minStr, maxStr] = band.split("-");
  const min = Number(minStr);
  const max = Number(maxStr);
  return CORRELATIONS
    .filter((c) => c.event === event && c.gender === gender && c.minMeters === min && c.maxMeters === max)
    .sort((a, b) => Math.abs(b.correlation) - Math.abs(a.correlation));
}

/** Get exercises ranked by |correlation| for a given event/gender/band */
export function getRankedExercises(
  event: EventCode,
  gender: GenderCode,
  band: string
): { exercise: string; type: ExerciseType; correlation: number; absCorrelation: number }[] {
  return getCorrelations(event, gender, band).map((c) => ({
    exercise: c.exercise,
    type: c.type,
    correlation: c.correlation,
    absCorrelation: Math.abs(c.correlation),
  }));
}
